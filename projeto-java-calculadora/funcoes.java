public class funcoes {

    public double somar(double x, double y) {
        double resultado;
        resultado = x+y;
        return resultado;

    }

    public double subtracao(double x, double y) {
        double resultado;
        resultado = x-y;
        return resultado;
    }

    public double multiplicacao(double x, double y) {
        double resultado;
        resultado = x*y;
        return resultado;
    }

    public double divisao(double x, double y) {
        double resultado;
        resultado = x/y;
        return resultado;
    }

}
